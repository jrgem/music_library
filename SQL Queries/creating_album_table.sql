-- Creating Album Table

CREATE TABLE album (
	artist_id INTEGER REFERENCES artist(artist_id),
	album_id SERIAL PRIMARY KEY,
	album_title VARCHAR(40) NOT NULL,
	album_genre VARCHAR(25) NOT NULL,
	release_date DATE NOT NULL
);


